package AdressBook;

public class Adress {
    private String person;
    private String city;
    private String street;
    private String houseNo;

    public Adress(String person, String city, String street, String houseNo) {
        this.person = person;
        this.city = city;
        this.street = street;
        this.houseNo = houseNo;
    }
}
